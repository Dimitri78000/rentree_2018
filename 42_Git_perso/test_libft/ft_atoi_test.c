#include <stdio.h>

int ft_atoi(char *str);

int main(void)
{
	printf("%d\n",ft_atoi("123456789"));
	printf("%d\n",ft_atoi("987654321"));
	printf("%d\n",ft_atoi("-2147483648"));
	printf("%d\n",ft_atoi("-42"));
	printf("%d\n",ft_atoi(" 42 42"));		// 0
	printf("%d\n",ft_atoi("abc42")); 		// 0
	return (0);
}
